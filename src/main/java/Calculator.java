public class Calculator {
    //access-modifier
    //data type
    //add (method name)

    //add
    public double add(double firstNumber, double secondNumber) { return firstNumber + secondNumber;
    }

    //subtract
    public double subtract(double firstNumber, double secondNumber) { return firstNumber - secondNumber;
    }

    //multiply

    public double multiply(double firstNumber, double secondNumber) { return firstNumber * secondNumber;

    }
    //divide
    public double divide(double firstNumber, double secondNumber) { return firstNumber / secondNumber;
    }


}
